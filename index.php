            function selecaoElemento(tipoBtn)
            {
                var limiteProximo = (listaItensTabela.length - 1);
                
                if(tipoBtn == 'anterior')
                {
                    if(elementoIdAtual > 0)
                    {
                        elementoIdAtual = (parseInt(elementoIdAtual) - parseInt(1));
                        
                        $("#desc-elemento-tabela-periodica").removeClass("modo-tabela-selecionada");
                        
                        setTimeout(function(){
                            
                            abrirLightbox(listaItensTabela[elementoIdAtual].id);
                        
                        }, 200);
                    }
                }
                if(tipoBtn == 'proximo')
                {
                    if(elementoIdAtual < limiteProximo)
                    {
                        elementoIdAtual = (parseInt(elementoIdAtual) + parseInt(1));
                        
                        $("#desc-elemento-tabela-periodica").removeClass("modo-tabela-selecionada");
                        
                        setTimeout(function(){
                            
                            abrirLightbox(listaItensTabela[elementoIdAtual].id);
                        
                        }, 200);
                    }
                }
